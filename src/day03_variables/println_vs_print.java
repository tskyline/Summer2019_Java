package day03_variables;

public class println_vs_print 
{
	public static void main(String[] args) 
	{
		System.out.println("Cubadama");				//println enters the next line.
		System.out.println("Jaba the Hut\n");
		
		System.out.println("========\t======\n");    // \t gives horizontal tab
		
		System.out.print("H'chu apenkee!");			//print writes one after another on the same line.
		System.out.print(" De wanna wanga.\n");
		
		System.out.println("\\Boska!");				//"\\" leaves a backslash
		System.out.println("\',\"");				// usage of ' and "
	}
}
